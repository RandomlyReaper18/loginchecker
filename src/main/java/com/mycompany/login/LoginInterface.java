    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText().trim();
        String password = String.valueOf(jPasswordField1.getPassword());

        if (UserManager.login(username, password)) {

            String date = LocalDate.now()
                    .format(DateTimeFormatter.ofPattern("MM/dd/yyyy"));

            String loginTime = LocalTime.now()
                    .format(DateTimeFormatter.ofPattern("hh:mm:ss a"));

            ArrayList<Attendance> attendance =
                    AttendanceManager.loadAttendance();

            boolean alreadyLoggedIn = false;

            for (Attendance a : attendance) {

                if (a.getUsername().equals(username)
                        && a.getDate().equals(date)
                        && a.getStatus().equals("Logged In")) {

                    alreadyLoggedIn = true;
                    break;
                }
            }

            if (alreadyLoggedIn) {

                JOptionPane.showMessageDialog(this,
                        "This user is already logged in.");

                loadTodayAttendance();
                return;
            }

            attendance.add(new Attendance(
                    username,
                    date,
                    loginTime,
                    "",
                    "Logged In"
            ));

            AttendanceManager.saveAttendance(attendance);

            loadTodayAttendance();

            JOptionPane.showMessageDialog(this,
                    "Login Successful!");

        }
        else if(UserManager.adminLogin(username, password)) {
            JOptionPane.showMessageDialog(this,
                        "Login Successful!");
            Administrator admin = new Administrator();
            admin.setVisible(true);
            dispose();
            
        } else {

            JOptionPane.showMessageDialog(this,
                    "Invalid Username or Password");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

